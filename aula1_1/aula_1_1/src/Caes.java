

public class Caes {
    String nome;
    int idade, rg;
    float peso;


    /*  metodo construtor*/
    public Caes() {
    }


    public String getNome() {
        return nome;
    }


    public void setNome(String nome) {
        this.nome = nome;
    }


    public int getIdade() {
        return idade;
    }


    public void setIdade(int idade) {
        this.idade = idade;
    }


    public int getRg() {
        return rg;
    }


    public void setRg(int rg) {
        this.rg = rg;
    }


    public float getPeso() {
        return peso;
    }


    public void setPeso(float peso) {
        this.peso = peso;
    }

        
    
    
}
